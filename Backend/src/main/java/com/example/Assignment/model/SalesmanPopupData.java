package com.example.Assignment.model;

public class SalesmanPopupData {
	public String salesmanName;
	public String salesmanArea;
	public double totalSalesAmount;
	public double totalSalesmanCommission;
	public String getSalesmanName() {
		return salesmanName;
	}
	public void setSalesmanName(String salesmanName) {
		this.salesmanName = salesmanName;
	}
	public String getSalesmanArea() {
		return salesmanArea;
	}
	public void setSalesmanArea(String salesmanArea) {
		this.salesmanArea = salesmanArea;
	}
	public double getTotalSalesAmount() {
		return totalSalesAmount;
	}
	public void setTotalSalesAmount(double totalSalesAmount) {
		this.totalSalesAmount = totalSalesAmount;
	}
	public double getTotalSalesmanCommission() {
		return totalSalesmanCommission;
	}
	public void setTotalSalesmanCommission(double totalSalesmanCommission) {
		this.totalSalesmanCommission = totalSalesmanCommission;
	}
}
